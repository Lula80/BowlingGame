CREATE SCHEMA IF NOT EXISTS bowling_club;



DROP TABLE IF EXISTS bowling_club.member_role;
CREATE TABLE bowling_club.member_role (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   name VARCHAR(255) NOT NULL,
   CONSTRAINT pk_member_role PRIMARY KEY (id)
);


CREATE TABLE  IF NOT EXISTS bowling_club.player (
 id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   name VARCHAR(255),
   CONSTRAINT pk_player PRIMARY KEY (id)
);
